// One-shot generator for data/campsites.seed.json.
//
// NOT shipped (this file lives in scripts/ but the seed JSON it emits is
// the actual deliverable). Re-run: `node scripts/gen-seed.mjs` from the
// repo root. The output is deterministic — no random/time-dependent
// behavior — so re-running over an existing file is safe.
//
// Strategy:
//   1) A curated list of well-known public-land campgrounds keyed by
//      agency/state. Each entry has a hand-picked amenity profile that
//      exercises one or more packing-engine branches.
//   2) Programmatic variation across remaining states/agencies to hit
//      every amenity branch and reach ≥150 entries.
//
// Every entry obeys the WS-0 contract:
//   - id  = `seed:<slug>`
//   - state = 2-char uppercase US postal code
//   - source = 'seed'
//   - amenities passes AmenitiesSchema
//
// The script intentionally produces visibly different lists by spreading
// amenity combos: backcountry/walk-in/drive-in × bear-country yes/no ×
// toilets none/vault/flush × cell none/weak/good × electricity yes/no, etc.

import { writeFileSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const OUT = resolve(__dirname, '..', 'data', 'campsites.seed.json')

function slug(s) {
  return s
    .toLowerCase()
    .replace(/&/g, 'and')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
}

function ams(overrides = {}) {
  return {
    potableWater: true,
    toilets: 'flush',
    showers: true,
    electricity: true,
    fireRings: true,
    firewoodAvailable: true,
    picnicTables: true,
    bearLockers: false,
    bearCountry: false,
    trashService: true,
    dumpStation: true,
    cellService: 'good',
    accessLevel: 'drive-in',
    ...overrides,
  }
}

function mk(name, opts) {
  const { agency, state, lat, lng, description, amenities, activities } = opts
  return {
    id: `seed:${slug(name)}-${slug(state)}`,
    name,
    agency,
    state,
    lat,
    lng,
    description,
    amenities,
    activities,
    source: 'seed',
  }
}

