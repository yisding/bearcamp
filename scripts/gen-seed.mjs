function fillFor(state, agency, idx) {
  // Cycle amenity profiles to exercise branches.
  const profiles = [
    {
      toilets: 'vault',
      showers: false,
      electricity: false,
      firewoodAvailable: false,
      cellService: 'none',
      bearCountry: false,
    },
    {
      toilets: 'flush',
      showers: false,
      electricity: false,
      cellService: 'weak',
      bearCountry: false,
    },
    {
      toilets: 'flush',
      showers: true,
      electricity: true,
      cellService: 'good',
      dumpStation: true,
      bearCountry: false,
    },
    {
      toilets: 'none',
      potableWater: false,
      showers: false,
      electricity: false,
      fireRings: false,
      firewoodAvailable: false,
      picnicTables: false,
      trashService: false,
      dumpStation: false,
      cellService: 'none',
      accessLevel: 'walk-in',
      bearCountry: false,
    },
  ]
  const overrides = profiles[idx % profiles.length]
  const [lat, lng] = STATE_CENTROIDS[state]
  const variant = idx % profiles.length
  const name = `${state} ${agency} Recreation Site ${idx + 1}`
  return mk(name, {
    agency,
    state,
    lat: lat + idx * 0.01,
    lng: lng - idx * 0.01,
    description: `${agency} managed CG in ${state}.`,
    amenities: ams(overrides),
    activities: variant === 3 ? ['hiking', 'backpacking'] : ['hiking', 'fishing'],
  })
}

const fillers = []
const fillerStates = Object.keys(STATE_CENTROIDS)
const fillerAgencies = ['USFS', 'BLM', 'USACE', 'State Park']
for (const state of fillerStates) {
  for (let i = 0; i < 2; i++) {
    const ag = fillerAgencies[(state.charCodeAt(0) + i) % fillerAgencies.length]
    fillers.push(fillFor(state, ag, i))
  }
}

const all = [...curated, ...fillers]

// De-duplicate by id (in case the slug collides somewhere).
const seen = new Set()
const out = []
for (const c of all) {
  if (seen.has(c.id)) continue
  seen.add(c.id)
  out.push(c)
}

writeFileSync(OUT, JSON.stringify(out, null, 2) + '\n')
console.log(`Wrote ${out.length} entries → ${OUT}`)
